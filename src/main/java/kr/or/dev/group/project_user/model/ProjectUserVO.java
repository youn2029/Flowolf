package kr.or.dev.group.project_user.model;

import java.util.Date;

public class ProjectUserVO {

	private int pro_no;					// 프로젝트번호(pk)
	private String mem_id;				// 참여자ID(pk)
	private String pro_user_man_chk;	// 관리자유무		
	private Date pro_user_date;			// 참여일
	private String mem_nick;			// 작성자Nick
	private String pro_user_color;		// 프로젝트색상
	
	public int getPro_no() {
		return pro_no;
	}

	public void setPro_no(int pro_no) {
		this.pro_no = pro_no;
	}

	public String getMem_id() {
		return mem_id;
	}

	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}

	public String getPro_user_man_chk() {
		return pro_user_man_chk;
	}

	public void setPro_user_man_chk(String pro_user_man_chk) {
		this.pro_user_man_chk = pro_user_man_chk;
	}

	public Date getPro_user_date() {
		return pro_user_date;
	}

	public void setPro_user_date(Date pro_user_date) {
		this.pro_user_date = pro_user_date;
	}

	public String getMem_nick() {
		return mem_nick;
	}

	public void setMem_nick(String mem_nick) {
		this.mem_nick = mem_nick;
	}

	public String getPro_user_color() {
		return pro_user_color;
	}

	public void setPro_user_color(String pro_user_color) {
		this.pro_user_color = pro_user_color;
	}	
	
}
