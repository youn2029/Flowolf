package kr.or.dev.group.box.model;

import java.util.Date;

public class BoxVO {
	
	private int box_no;			// 보관함번호
	private String box_name;	// 보관함명
	private int box_index;		// 조회순번
	private String box_del_chk;	// 삭제여부
	private Date box_date;		// 등록일
	private String mem_id;		// 회원ID
	private int pro_box_chk;	// 해당 프로젝트의 보관함 유무(vo에서만 사용) 

	public int getBox_no() {
		return box_no;
	}

	public void setBox_no(int box_no) {
		this.box_no = box_no;
	}

	public String getBox_name() {
		return box_name;
	}

	public void setBox_name(String box_name) {
		this.box_name = box_name;
	}

	public int getBox_index() {
		return box_index;
	}

	public void setBox_index(int box_index) {
		this.box_index = box_index;
	}

	public Date getBox_date() {
		return box_date;
	}

	public void setBox_date(Date box_date) {
		this.box_date = box_date;
	}

	public String getMem_id() {
		return mem_id;
	}

	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}

	public String getBox_del_chk() {
		return box_del_chk;
	}

	public void setBox_del_chk(String box_del_chk) {
		this.box_del_chk = box_del_chk;
	}

	public int getPro_box_chk() {
		return pro_box_chk;
	}

	public void setPro_box_chk(int pro_box_chk) {
		this.pro_box_chk = pro_box_chk;
	}	
	
}
