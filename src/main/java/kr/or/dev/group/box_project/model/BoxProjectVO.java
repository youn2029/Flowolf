package kr.or.dev.group.box_project.model;

import java.util.Date;

public class BoxProjectVO {
	
	private int pro_no;			// 프로젝트번호
	private int box_no;			// 보관함번호	
	private Date box_pro_date;	// 등록일

	public int getPro_no() {
		return pro_no;
	}

	public void setPro_no(int pro_no) {
		this.pro_no = pro_no;
	}

	public int getBox_no() {
		return box_no;
	}

	public void setBox_no(int box_no) {
		this.box_no = box_no;
	}

	public Date getBox_pro_date() {
		return box_pro_date;
	}

	public void setBox_pro_date(Date box_pro_date) {
		this.box_pro_date = box_pro_date;
	}
	
}
