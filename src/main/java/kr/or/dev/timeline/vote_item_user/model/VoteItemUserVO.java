package kr.or.dev.timeline.vote_item_user.model;

import java.util.Date;

public class VoteItemUserVO {
	
	private int vi_no;			// 투표항목번호 (pk)
	private String mem_id;		// 투표자ID (pk)
	private Date viu_date;		// 투표일
	
	public int getVi_no() {
		return vi_no;
	}
	
	public void setVi_no(int vi_no) {
		this.vi_no = vi_no;
	}
	
	public String getMem_id() {
		return mem_id;
	}
	
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	
	public Date getViu_date() {
		return viu_date;
	}
	
	public void setViu_date(Date viu_date) {
		this.viu_date = viu_date;
	}

}
