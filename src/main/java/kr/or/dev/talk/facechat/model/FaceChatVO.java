package kr.or.dev.talk.facechat.model;

import java.util.Date;

public class FaceChatVO {
	
	private int fc_no;		// 화상회의번호
	private Date fc_date;	// 개설일	
	private int pro_no;		// 프로젝트번호
	private String fc_ip;	// 개설자IP	
	private int fc_port;	// 개설자Port번호

	public int getFc_no() {
		return fc_no;
	}

	public void setFc_no(int fc_no) {
		this.fc_no = fc_no;
	}

	public Date getFc_date() {
		return fc_date;
	}

	public void setFc_date(Date fc_date) {
		this.fc_date = fc_date;
	}

	public int getPro_no() {
		return pro_no;
	}

	public void setPro_no(int pro_no) {
		this.pro_no = pro_no;
	}

	public String getFc_ip() {
		return fc_ip;
	}

	public void setFc_ip(String fc_ip) {
		this.fc_ip = fc_ip;
	}

	public int getFc_port() {
		return fc_port;
	}

	public void setFc_port(int fc_port) {
		this.fc_port = fc_port;
	}
	
}
